export function generatePromoCode(telegramId: string): string {
  // Generate a promo code based on the Telegram ID
  const last3Digits = parseInt(telegramId.slice(-3)) || 0;
  const numberPart = Math.floor(parseInt(telegramId) / 1000);

  if (isNaN(last3Digits) || isNaN(numberPart)) {
    console.error('Invalid Telegram ID for promo code generation:', telegramId);
    return '';
  }

  // Select a word from the list based on the last three digits of the ID
  const wordIndex = last3Digits % oneThousandWords.length;
  const selectedWord = oneThousandWords[wordIndex];

  // Construct the promo code
  const promoCode = `${selectedWord.toLowerCase()}${numberPart}`;
  return promoCode;
}

